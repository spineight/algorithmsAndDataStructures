  std::vector<size_t> grasshopper_and_money_restoring_sequence_of_jumps(
          size_t n, size_t k, const std::vector<int> money) {
    vector<int> a(n+1,0);
    vector<size_t> from(n+1,0);
    a[0] = money[0];
    for(size_t i = 1; i <= n; ++i) {
      a[i] = a[i-1] + money[i];
      from[i] = i-1;
      for(size_t j = 2; j <= min(i,k); ++j) {
        if(a[i] < a[i-j] + money[i]) {
          a[i] = a[i-j] + money[i];
          from[i] = i - j;
        }
      }
    }
    std::vector<size_t> idxs;
    size_t i = n;
    while(i != 0) {
      idxs.push_back(i);
      i = from[i];
    }
    idxs.push_back(0);
    reverse(begin(idxs), end(idxs));
    return idxs;
  }

