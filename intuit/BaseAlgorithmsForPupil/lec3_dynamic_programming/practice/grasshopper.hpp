// =======================================================
// ### Кузнечик может прыгать на 1 и 2 столбика вперед
// (похоже на числа Фибоначи)
// =======================================================
//              *   *   X
//  |___|___|___|___|___|
//  0   1   2   3   4   n
//! Кузнечик начинает с 0-ого столбика
//! @param n - столбик для которого нужно допрыгать кузнечику
//! @returns число способов допрыгать
//! Кузнечик может прыгать на 1 и 2 столбика вперед
  size_t grasshopper_1_2(size_t n) {
    if(n == 0) return 1;
    vector<size_t> a (n+1,0);
    a[0] = 1;
    a[1] = 1;
    for(size_t i = 2; i <=n; ++i) {
      a[i] = a[i-2] + a[i-1];
    }
    return a[n];
  }

// =======================================================
// ### Кузнечик может прыгать на 1,2 ... k столбиков вперед
// ? Число способов допрыгать до n-ого столбика
// =======================================================
  //! @param n - столбик на который нужно попасть, начиная с 0-ого столбика
  //! @param k - кузнечик может делать прыжки длиной 1..k
  //! @returns количество способов
  size_t grasshopper(size_t n, size_t k) {
    vector<size_t> a(n+1,0);
    a[0] = 1;
    for(size_t i = 1; i <= n; ++i) {
      for(size_t j = 1; j <= min(i,k); ++j) {
        a[i] += a[i-j];
      }
    }
    return a[n];
  }


// =======================================================
// ### Кузнечик может прыгать на 1,2 ... k столбиков вперед
// ### + на некоторых столбиках сидят лягушки
// ? Число способов допрыгать до n-ого столбика
// =======================================================

//! @param frog_at_pos_i[k] == true of there is a frog at pos k
  size_t grasshopper_and_frogs(size_t n, size_t k,
                               const std::vector<bool> frog_at_pos_i) {
    if(frog_at_pos_i.front()) return 0;
    vector<size_t> a(n+1,0);
    a[0] = 1;
    for(size_t i = 1; i <= n; ++i) {
      for(size_t j = 1; j <= min(i,k) && !frog_at_pos_i[i]; ++j) {
        a[i] += a[i-j];
      }
    }
    return a[n];
  }

