// ## Вектор.
// Геомитрический объект, который характиризуется двумя параметрами: направление и длина
// Можно сказать, что это отрезок, которому задали направление
//
// Отложим этот отрезок от точки (0,0) - тогда его конец окажется в какой-то точке,
// эта точка и называется координатами вектора
//
// ### Зачем нужны вектора
// - Осуществлять перемещения
// - Вычислять углы (чтобы с ними общаться нам понадобится ввести такое понятие как тригонометрические функции)
//
// ### Действия с векторами
// Длина: по теореме Пифагора len(V) = |V| = sqrt(x^2+y^2) = std::hypot(x,y)
//
// Сложение двух векторов: один из них отложить от конца другого и после этого из начала первого провести вектор в конец второго,
// получим вектор, который является суммой этих двух.
// Это можно так понять: можно считать, что мы совершаем какое-то перемещение, сначала мы совершили одно перемещение, затем другое
// r0(x1,y1) + r1(x2,y2) = r3(x1+x2, y1+y2)
//
// Умножение вектора на число
// c*r1 = r(cx1,cy1)
//
// Говорят, что вектор нормирован если его длина == 1,|r| = 1
// Любой вектор кроме нулевого (0,0) можно нормировать
// r / |r| - получется вектор, который смотрит в том же направлении, но имеет длину 1
//
//

// Особой разницы в смысле представления в программе между точкой и вектором нет
