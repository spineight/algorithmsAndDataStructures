//  A. Книги
//          ограничение по времени на тест
//  2 секунды
//          ограничение по памяти на тест
//  256 мегабайт
//          ввод
//  стандартный ввод
//  вывод
//          стандартный вывод
//
//          Когда у Валеры появляется свободное время, он идет в библиотеку и читает книги.
//          Вот и сегодня у него появилось t свободных минут для чтения.
//          Поэтому Валера взял n книг в библиотеке и для каждой книги оценил: какое время потребуется, чтобы ее прочитать.
//          Пронумеруем книги целыми числами от 1 до n. Для прочтения i-той книги Валере требуется ai минут.
//
//  Валера решил, что он выберет произвольную книгу с номером i и будет читать книги друг за другом, начиная с этой книги.
//  Другими словами, сначала он прочитает книгу c номером i, затем книгу с номером i + 1, затем книгу с номером i + 2 и так далее.
//  Он продолжает этот процесс до тех пор, пока либо не закончится его свободное время, либо не прочитает книгу с номером n.
//  Каждую книгу Валера читает целиком, то есть он не читает книгу, которую не успеет дочитать до конца из-за нехватки свободного времени.
//
//  Посчитайте максимальное количество книг, которое Валера сможет прочитать.
//  Входные данные
//
//  В первой строке заданы два целых числа n и t (1 ≤ n ≤ 10^5; 1 ≤ t ≤ 10^9) — количество книг и количество свободных минут у Валеры соответственно.
//  Во второй строке задана последовательность из n целых чисел a1, a2, ..., an (1 ≤ ai ≤ 104),
//  в которой число ai обозначает количество минут, которое требуется для прочтения i-ой книги.
//  Выходные данные
//
//  Выведите единственное целое число — максимальное количество книг, которое Валера сможет прочитать.
//  Примеры
//          Входные данные
//          Скопировать
//
//  4 5
//  3 1 2 1
//
//  Выходные данные
//  Скопировать
//
//  3
//
//  Входные данные
//  Скопировать
//
//  3 3
//  2 2 3
//
//  Выходные данные
//  Скопировать
//
//  1
  void solve_books() {
    long long n,t;
    cin >> n >> t;
    vector<long long> a(n);
    for(auto& v : a) cin >> v;

    int l{0};
    long long time{0};
    long long res{0};
    for(int r = 0; r < n; ++r) {
      time += a[r];
      while(time > t) {
        time -= a[l];
        ++l;
      }
      res = max(res, r-l+1);
    }
    cout << res;
  }

