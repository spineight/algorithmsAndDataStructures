  void solve_balanced_team() {
    long long n;
    cin >> n;
    vector<long long> a(n);
    for(auto& v : a) cin >> v;

    sort(begin(a), end(a));

    int l{0};
    int res{0};
    for(int r = 0; r < n; ++r) {
      while(a[r]-a[l] > 5) {
        ++l;
      }
      res = max(res, r-l+1);
    }
    cout << res;
  }

