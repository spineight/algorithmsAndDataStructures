//
//          В первом тестовом примере только одно возможное разбиение, которое максимизирует sum1
//  : [1,3,1],[ ],[1,4]
//
//  .
//
//  Во втором тестовом примере существует единственный способ набрать sum1=4
//  : [1,3],[2,1],[4]
//
//  .
//
//  В третьем тестовом примере есть только один способ разделить массив: [ ],[4,1,2],[ ]
//  .

//! Идеи
// |s1  | s2  | s3|
// Мы ищем отрезок S2 с концами L,R
  void solve_3_parts_of_array() {
    int n;
    cin >> n;
    vector<long long> a(n);
    for(auto& v : a) cin >> v;

    long long s1{0}, s3{0};
    int r{n};
    long long res{0};
    for(int l = 0; l < n; ++l) {
      s1 += a[l];
      while(s3 < s1) {
        --r;
        s3 += a[r];
      }
      if(s3==s1 && l < r)
        res = max(res, s1);
    }
    cout << res;
  }

  //! https://codeforces.com/group/pgkaqF4igo/contest/256854/problem/D
//  D. Вася и робот
//          ограничение по времени на тест
//  1 секунда
//          ограничение по памяти на тест
//  256 мегабайт
//          ввод
//  стандартный ввод
//  вывод
//          стандартный вывод
//
//          У Васи есть робот, расположенный на бесконечной плоскости. Изначально робот находится в стартовой клетке (0,0)
//
//  . Робот может выполнять команды. Существует четыре основных команды, доступные для выполнения:
//
//  U — перейти из клетки (x,y)
