//  целых чисел d1,d2,…,dn (1≤di≤109) — элементы массива d
//
//  .
//  Выходные данные
//
//  Выведите одно целое число — максимально возможное значение sum1
//  , удовлетворяющее условию sum1=sum3
//
//          .
//
//                  Очевидно, всегда существует хотя бы один способ разбить массив нужным образом (если a=c=0
//  и b=n
//
//  ).
//  Примеры
