//
//          В первом тестовом примере измененная последовательность выглядит следующим образом: LULUU. Таким образом, длина измененного подотрезка равна 3−1+1=3
//
//  .
//
//  Во втором тестовом примере изначальная последовательность команд приводит робота в точку (x,y)
//  , поэтому длина измененного подотрезка равна 0
//
//  .
//
//  В третьем тестовом примере робот никак не сможет попасть в точку (x,y)
//  .
  void solve_VasiaAndRobot() {
    int n;
    cin >> n;
    string s;
    cin >> s;
    long long x,y;
    cin >> x >> y;

    vector<int> dx(n);
    vector<int> dy(n);

    for(int i = 0; i < n; ++i) {
      if(s[i] == 'U') dy[i]=1;
      if(s[i] == 'D') dy[i]=-1;
      if(s[i] == 'R') dx[i]=1;
      if(s[i] == 'L') dx[i]=-1;
    }

    long long xx{0},yy{0};

    for (int i = 0; i < n; ++i) {
      xx += dx[i];
      yy += dy[i];
    }
    if((x+y+n) % 2 !=0 || abs(x) + abs(y) >n) {
      cout << -1 << '\n';
      return 0;
    }
    if(xx == x && yy == y) {
      cout << 0 << '\n';
      return 0;
    }

    int l{0};
    int res{n};
    for(int r = 0; r < n; ++r) {
      xx -= dx[r];
      yy -= dy[r];
      while(abs(xx-x) + abs(yy-y) <= r-l+1) {
        xx += dx[l];
        yy += dy[l];
        ++l;
      }
      if(l>0)
        res = min(res, r-l+2);
    }
    cout << res;
  }
